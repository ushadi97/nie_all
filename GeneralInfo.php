    <h2 style="background-color: #bdbdbd">ESTABLISHMENT</h2>
    <p>The National Institute of Education (NIE), Sri Lanka was established in 1986 under the provisions ofthe National Institute of Education
        Act No. 28 of 1985 and commenced operations at No. 255, Bauddhaloka Mawatha, Colombo 07. The main purpose of creating the NIE was to establish a
        unique institute for capacity building of educational managers, teacher educators and teachers, design and develop school curricula and conduct
        policy research on education. The NIE was mandated to advise the Minister on matters related to the development of education in Sri Lanka. The
        objectives of the Institute, as given in the above Act are as follows:</p>
    <ul>
        <li>Advise the Minister regarding plans, programmes and activities for the development of education in Sri Lanka.</li>
        <li> Provide and promote postgraduate education in the several specialties of education.</li>
        <li>Conduct and promote studies on the education system including its performance, goals, structures, content and methodology and on
            the social, economic and other aspects of education.</li>
        <li>Initiate and promote innovative practices in the education system, including adaptation of technology for educational purposes.</li>
        <li>Provide for the development of professional and general competence of personnel in the education system.</li>
        <li>Make available to the Government and other approved organizations, specialist services in education.</li>
        <li>Carry out education development programmes approved by the Minister.</li>
        <li>Co-ordinate with other institutions having similar objectives.</li>
    </ul>
    <h2 style="background-color: #bdbdbd">CORPORATE OBJECTIVES</h2>
    <ul>
        <li>Assuring quality education through a balanced curriculum for the development of productive citizens competent to face local and global demands.</li>
        <li> Capacity building of professionals to provide leadership in educational development.</li>
        <li>Ensuring a scientific information base to facilitate policy making and improve practice in education.</li>
        <li>Supporting total educational improvement through utilization of modern technologies</li>
        <li>Promote economic, social and cultural development through innovative participatory approaches in the education system.</li>
        <li>Develop the NIE as an educational friendly institute for administration and development</li>
        <li>Develop child friendly, teacher friendly schools where democracy and human values are perpetuated and sustained.</li>
    </ul>
    <p>To achieve the above objectives and ensure efficient functioning of the institute, the structure of the Institute has been reorganized.
